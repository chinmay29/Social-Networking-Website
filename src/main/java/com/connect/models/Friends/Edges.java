package com.connect.models.Friends;

public class Edges {
	private int U1;
	private int U2;

	public Edges() {
		super();
	}
	public Edges(int u1, int u2) {
		super();
		U1 = u1;
		U2 = u2;
	}
	public int getU1() {
		return U1;
	}
	public void setU1(int u1) {
		U1 = u1;
	}
	public int getU2() {
		return U2;
	}
	public void setU2(int u2) {
		U2 = u2;
	}

}
